<p>「雅施精挑細選購物優惠」除了提供消費者便利的購物環境外，我們也非常重視您的隱私權。我們依從香港特別行政區《個人資料(私隱)條例》(第486章)訂定及在可行的情況之下提供保障。因此訂定有關隱私權保護聲明。請您詳細閱讀下列有關隱私權保護內容。</p>
 
<h3>個人資料取得</h3>
<p>當使用者進入「雅施精挑細選購物優惠」時，基本上並不需要輸入個人資料。除非您要加入本站會員或參加活動，否則「雅施精挑細選購物優惠」不會在使用者不知情的情況下，取得使用者之個人資料。</p>
<p>「雅施精挑細選購物優惠」會記錄使用者上站的位址，以及在網站內瀏覽活動等相關資料，但這些資料僅供作為流量分析和網路行為調查，以便於改善本網站的服務品質。</p>
<p>在某些情況下，例如當使用者要求加入會員、訂閱電子報、參加活動或使用其他服務時，「雅施精挑細選購物優惠」可能會要求使用者登記個人資料，以便於聯繫、完成交易、提供服務、或處理訂閱程序。</p>
 
