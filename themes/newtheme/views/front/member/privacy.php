<p>為了便利使用者，「雅施精挑細選購物優惠」可能會使用曲奇(cookie)技術，以便於提供更適合使用者個人需要的服務；曲奇(cookie)是網站主機用來和使用者瀏覽器進行溝通的一種技術，它可能在使用者的電腦中儲存某些資訊，但使用者可以經由瀏覽器的設定，取消、或限制此項功能。</p>
 
<h3>資料安全</h3>
<p>為保障您個人的隱私及安全，您在「雅施精挑細選購物優惠」內之帳號資料都以密碼保護。</p>
<p>在部分情況下，「雅施精挑細選購物優惠」採用世界先進的Secure Socket Layer (SSL)傳輸加密機制，能避免您的資料在傳送時被第三者讀取，以保障資料安全。</p>
<p>我們不會未經閣下同意出售或公開您的個人資料，除以下情況：</p>
