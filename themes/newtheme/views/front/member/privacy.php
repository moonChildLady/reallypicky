<p>「雅施精挑細選購物優惠」及其相關網站所取得的個人資料，僅供「雅施精挑細選購物優惠」內部依照原說明的使用目的和範圍。除非事先說明或依照相關法律規定，否則「雅施精挑細選購物優惠」絕對不會將使用者的個人資料提供給第三者之用。</p>
<p>成為會員即代表願意接受由亞洲化粧品企業有限公司及「雅施精挑細選購物優惠」發出之推廣活動消息、資訊及優惠。如果您不想收到任何雅施化粧品中心及「雅施精挑細選購物優惠」的資料，請電郵至<a href="mailto:reallypicky@aster.com.hk">reallypicky@aster.com.hk</a>並在主旨註明「停止接收相關推廣信息」。</p>
 
